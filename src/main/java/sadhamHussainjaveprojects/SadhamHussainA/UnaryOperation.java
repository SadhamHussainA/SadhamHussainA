package sadhamHussainjaveprojects.SadhamHussainA;

public class UnaryOperation {

	public static void main(String[] args) {
		
		int hi=98; char alpha='A';
		
		System.out.println(++hi);
		System.out.println(alpha);
		System.out.println(alpha++);
		
		System.out.println(hi++);
		System.out.println(hi);
		
		int google=129; char yahoo='R';
		
		System.out.println("I need increament Value for this");
		
		System.out.println(yahoo);
		System.out.println(yahoo++);
		System.out.println(yahoo+4);
		
		System.out.println(google++);
		System.out.println(google);
		System.out.println(--google);
		
		System.out.println(google+12458);
	}
}
